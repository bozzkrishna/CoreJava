package Inheritance.parentchild;

public class MyTime extends MyDate {
	private int hh,mm,ss;

	public int getHh() {
		return hh;
	}

	public void setHh(int tt) {
		this.hh = tt;
	}

	public int getMm() {
		return mm;
	}

	public void setMm(int mm) {
		this.mm = mm;
	}

	public int getSs() {
		return ss;
	}

	public void setSs(int ss) {
		this.ss = ss;
	}

}
