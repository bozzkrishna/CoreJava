package JavaCore;

public class team {
	

		
		private String teamName;
		private  String teamCapt;
		private  int teamSize;
		private int teamAchive;
		
		public String getTeamName()
		 {
			return teamName;
			
		 }
		public void setTeamName(String teamName)
		{
			this.teamName = teamName;
		}
		
		
		public String getTeamCapt()
		{
			return teamCapt;
		}
		public void setTeamCapt(String teamCapt)
		{
			this.teamCapt = teamCapt;
		}
		
		
		public int getTeamSize()
		{
			return teamSize;
		}
		public void setTeamSize(int teamSize)
		{
			this.teamSize = teamSize;
		}
		public int getTeamAchive()
		{
			return teamAchive;
		}
		public void setTeamAchive(int teamAchive)
		{
			this.teamAchive = teamAchive;
		}
		
}
