package JavaCore;

public class Item {

	private int itemid;
	private String name;
	private float price;
	
	public int getItemid()
	{
		return itemid;
	}
	public void setItemid(int itemid)
	{
		this.itemid=itemid;
	}
	public String getName()
	{
		return name;
	}
	public void setName(String name)
	{
		this.name=name;
	}
	public float getPrice()
	{
		return price;
	}
	public void setPrice(float price)
	{
		this.price=price;
	}
	
}
